/**
 * Инициализирует функционал поиска
 *
 * @param {string} searchField - Имя поля состояния, содержащего поисковый запрос
 * @returns {Function} - Функция обработки поискового запроса
 */
export function initSearching(searchField) {
    /**
     * Обрабатывает поисковый запрос
     *
     * @param {Object} query - Текущий запрос
     * @param {Object} state - Состояние приложения
     * @param {Object} action - Действие (не используется в данной реализации)
     * @returns {Object} - Обновленный запрос с параметрами поиска
     */
    return (query, state, action) => {
        // Проверяем, есть ли значение в поле поиска
        if (state[searchField]) {
            // Если значение есть, добавляем его в запрос
            return Object.assign({}, query, {
                search: state[searchField], // Устанавливаем параметр поиска
            });
        } else {
            // Если поле поиска пустое, возвращаем исходный запрос
            return query;
        }
    };
}

/**
 * Логика работы функции:
 * 1. Функция принимает имя поля, где хранится поисковый запрос
 * 2. Возвращает функцию-обработчик, которая:
 *    - принимает текущий запрос, состояние и действие
 *    - проверяет наличие поискового запроса в состоянии
 *    - если запрос есть, добавляет его в параметры запроса
 *    - если запроса нет, возвращает исходный запрос без изменений
 */
